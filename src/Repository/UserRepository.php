    /**
     * @param User $user
     * @param Ad $ad
     * @param EntityManagerInterface $entityManager
     * @return User|null
     */
    public function addToFavorites(User $user, Ad $ad, EntityManagerInterface $entityManager): ?User
    {
        $u = $this->findOneBy(['id' => $user->getId()]);
        $u->addFavorite($ad);
        $entityManager->persist($u);
        $entityManager->flush();

        return $u;

